// Tuples are arrays with a fixed length and a fixed type of elements.

// Example:
const person: [string, number] = ["Max", 21];
console.log(person[0]);

const person2: [string, number, string, number] = [
    "John",
    30,
    "New York",
    123456789,
]
console.log(person2);